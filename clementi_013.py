# Scrivere 10 volte la parola "ciao"
for _ in range(10):
    print("ciao")

# Sommare i numeri da 10 a 20
somma1 = sum(range(10, 21))
print("La somma dei numeri da 10 a 20 è:", somma1)

# Sommare i numeri pari fino a 30
somma2 = sum(range(0, 31, 2))
print("La somma dei numeri pari fino a 30 è:", somma2)

# Moltiplicare i numeri da 1 a 10
prodotto = 1
for num in range(1, 11):
    prodotto *= num
print("Il prodotto dei numeri da 1 a 10 è:", prodotto)